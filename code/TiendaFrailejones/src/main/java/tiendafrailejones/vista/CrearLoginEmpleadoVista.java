    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        empleado = controladorEmpleado.existePorId(Long.valueOf(inputIDEmpleado.getText()));
        if (empleado.getId() == null) {
            labelUsuarioNoExiste.setText("Empleado no existe");
        } else {
            labelidUsuario.setText(empleado.getIdentificacion());
            labelNombre.setText(empleado.getNombre());
            inputIDEmpleado.setText("");
            btnAsignar.setEnabled(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btnVolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVolverActionPerformed
        // TODO add your handling code here:
        AdministrarUsuarios administrarUsuarios = new AdministrarUsuarios();
        administrarUsuarios.setVisible(true);
        this.dispose();
        limpiarCampos();
    }//GEN-LAST:event_btnVolverActionPerformed

