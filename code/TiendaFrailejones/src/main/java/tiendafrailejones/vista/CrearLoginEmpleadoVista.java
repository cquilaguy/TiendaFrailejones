    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        // TODO add your handling code here:

        try {
            
            verificarCampos();
            
            login.setUser(inputUsuario.getText());
            login.setPassword(inputPassword.getText());
            login.setUserType("EMPLEADO");
            login.setIdUsuario(empleado.getId());
            btnAsignar.setEnabled(false);

            controladorLogin.crearLoginParaUsaurio(login);
            limpiarCampos();
            JOptionPane.showMessageDialog(null, "Se ha guardado exitosamente");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Debe ingresar" + e);
        }


    }//GEN-LAST:event_btnAsignarActionPerformed

    private void verificarCampos() throws Exception {
        if (inputUsuario.getText().isEmpty() || inputUsuario.getText() == null) {
            throw new Exception("usuario");
        } else if (inputPassword.getText().isEmpty() || inputPassword.getText() == null) {
             throw new Exception("constrase√±a");
        }
    }

    private void limpiarCampos() {
        labelNombre.setText("");
        labelidUsuario.setText("");
        inputUsuario.setText("");
        inputPassword.setText("");
        empleado = null;
    }

